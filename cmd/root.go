package cmd

import (
	"fmt"
	"os"

	"github.com/spf13/cobra"
)

var cfgFile string
var threads int

// rootCmd represents the base command when called without any subcommands
var rootCmd = &cobra.Command{
	Use:   "dutil",
	Short: "Docker utility tool to modify and push existing Docker images",
}

func Execute() {
	if err := rootCmd.Execute(); err != nil {
		fmt.Println(err)
		os.Exit(1)
	}
}

func init() {
	rootCmd.PersistentFlags().StringVar(&cfgFile, "config", "", "config file (default \"$HOME/.dutil.yaml\")")
	rootCmd.PersistentFlags().IntVarP(&threads, "threads", "t", 8, "Number of concurrent Docker pushes")
}
